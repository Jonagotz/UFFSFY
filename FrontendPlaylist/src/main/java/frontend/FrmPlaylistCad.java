    private void initOtherComponents() {
        musicaListModel = new DefaultListModel<>();
        musicaJList = new JList<>(musicaListModel);
       
        // Adiciona as músicas de exemplo
        List<Musica> musicas = getMusicas();
        for (Musica musica : musicas) {
            musicaListModel.addElement(musica);
        }
        
        // Adiciona a JList a um JScrollPane para permitir rolar
        JScrollPane scrollPane = new JScrollPane(musicaJList);
        scrollPane.setPreferredSize(new Dimension(200, 100));

        // Adiciona os componentes ao frame
        add(scrollPane);
    }
    
    private List<Musica> getMusicas() {
        RestTemplate req = new RestTemplate();
        
        ResponseEntity<Musica[]> response = req.getForEntity(
                "http://localhost:8080/musicas", Musica[].class);
        
        Musica[] musicas = response.getBody();
        
        return Arrays.asList(response.getBody());
    }
    
    private void btnCadastrarPlaylistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarPlaylistActionPerformed
        Playlist playlist = new Playlist();

        // Obter índices das músicas selecionadas
        int[] selectedIndices = musicaJList.getSelectedIndices();

        // Criar uma lista para armazenar as músicas selecionadas
        List<Musica> selectedMusicas = new ArrayList<>();

        // Obter as músicas correspondentes aos índices selecionados
        for (int selectedIndex : selectedIndices) {
            selectedMusicas.add(musicaListModel.getElementAt(selectedIndex));
        }
         
        DateTimeFormatter formatoData = DateTimeFormatter.ofPattern("dd-MM-yyyy HH:mm");
 
        String dataCriacao = LocalDateTime.now().format(formatoData);
        
        playlist.setMusicas(selectedMusicas);
        playlist.setNome(txtNomePlaylists.getText());
        playlist.setDataCriacao(dataCriacao);

        System.out.println(playlist);
        
        try {
            RestTemplate req = new RestTemplate();

            req.postForObject("http://localhost:8080/playlist", playlist, Playlist.class);
            JOptionPane.showMessageDialog(this, "Playlist salva com sucesso!");
        } catch (RestClientException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            System.out.println(e.getMessage());
        }
        
        this.dispose();
        FrmPlaylistLista frm = new FrmPlaylistLista();
        frm.setVisible(true);
    }//GEN-LAST:event_btnCadastrarPlaylistActionPerformed

