    private void showDialog() {
        JDialog dialog = new JDialog(this, "Selecione Músicas", true);

        JButton btnOk = new JButton("OK");
        btnOk.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                dialog.dispose();
                List<Musica> selectedMusicas = musicaJList.getSelectedValuesList();
                System.out.println("Musicas selecionadas: " + selectedMusicas);
            }
        });

        dialog.setLayout(new BorderLayout());
        dialog.add(new JScrollPane(musicaJList), BorderLayout.CENTER);
        dialog.add(btnOk, BorderLayout.SOUTH);
        dialog.setSize(200, 200);
        dialog.setLocationRelativeTo(this);
        dialog.setVisible(true);
    }
    
