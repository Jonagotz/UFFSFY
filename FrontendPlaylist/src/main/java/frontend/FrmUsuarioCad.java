    private void btnCadastroUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroUsuarioActionPerformed
        Usuario usuario = new Usuario();
       
        try {
            usuario.setNome(txtNomeUsuario.getText());
            usuario.setEmail(txtEmailUsuario.getText());
            usuario.setSenha(Integer.valueOf(txtSenhaUsuario.getText()));
            usuario.setDataNasc(txtDataNasc.getText());
        
            RestTemplate req = new RestTemplate();
            
            req.postForObject("http://localhost:8080/usuario", usuario, Usuario.class);
            JOptionPane.showMessageDialog(this, "Cadastro de usuÃ¡rio realizado com sucesso!");
            
            this.dispose();
            Principal tela = new Principal();
            tela.setVisible(true);
        } catch (RestClientException | IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnCadastroUsuarioActionPerformed

