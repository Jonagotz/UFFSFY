    public Usuario obterUsuarioPorId(String id) {
        RestTemplate req = new RestTemplate();
        ResponseEntity<Usuario> response = req.getForEntity("http://localhost:8080/usuario/" + id, Usuario.class);
        Usuario usuario = response.getBody();
        if (usuario != null) {
            return usuario;
        } else {
            return null;
        }
    }
    
    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String UsuarioSelecionado = (String) cbUsuario.getSelectedItem();
        
        if (UsuarioSelecionado != null) {
            String[] partes = UsuarioSelecionado.split(" - ");
            if (partes.length == 2) {
                String idUsuario = partes[0];
                Usuario usuario = obterUsuarioPorId(idUsuario);
                int senhaUsuario = usuario.getSenha();
                String pwd = new String(txtSenha.getPassword());
                
                // verificando se a senha esta correta
                if (pwd.equals(String.valueOf(senhaUsuario))) {
                    FrmPlaylistLista frm = new FrmPlaylistLista();
                    frm.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Senha errada, por favor digite a senha correta.");
                }
                
            } else {
                JOptionPane.showMessageDialog(this, "Formato de usuário inválido.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum usuário selecionado.");
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed

