    public void loadUsuarios() {
        RestTemplate req = new RestTemplate();
        
        ResponseEntity<Usuario[]> response = req.getForEntity("http://localhost:8080/usuarios", Usuario[].class);
        
        Usuario[] usuarios = response.getBody();
        
        for (var c : usuarios) {
            cbUsuario.addItem(c.id + " - " + c.nome);
        }
    }
    
